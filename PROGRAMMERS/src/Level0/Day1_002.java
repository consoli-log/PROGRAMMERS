package Level0;

public class Day1_002 {

    public static void main(String[] args) {

        solution(1,2);

        System.out.println(solution(1,2));
    }

    public static int solution(int a, int b) {
        int num1 = a;
        int num2 = b;

        int answer = num1 - num2;

        return answer;
    }
}
